ACCEPT_LANGS = ["en-US,en;q=0.9", "en-GB,en;q=0.8", "en-US,en;q=0.7,es;q=0.3"]


def _parse_chrome_version(ua: str) -> str | None:
    import re

    m = re.search(r"Chrome/(\d+)", ua)
    return m.group(1) if m else None


def _platform(ua: str) -> str:
    if "Windows" in ua:
        return '"Windows"'
    if "Mac OS X" in ua or "Macintosh" in ua:
        return '"macOS"'
    if "Android" in ua:
        return '"Android"'
    if "iPhone" in ua or "iPad" in ua:
        return '"iOS"'
    return '"Linux"'


def headers_for(ua: str) -> Dict[str, str]:
    """Return a coherent header set matching the chosen UA."""
    h = {
        "User-Agent": ua,
        "Accept": "text/html,application/xhtml+xml,application/xml;q=0.9,image/avif,image/webp,*/*;q=0.8",
        "Accept-Language": random.choice(ACCEPT_LANGS),
        "Accept-Encoding": "gzip, deflate, br",
        "Connection": "keep-alive",
        "Upgrade-Insecure-Requests": "1",
    }
    cv = _parse_chrome_version(ua)
    if cv and "Edg/" not in ua:
        h["Sec-CH-UA"] = f'"Chromium";v="{cv}", "Google Chrome";v="{cv}", "Not.A/Brand";v="24"'
        h["Sec-CH-UA-Mobile"] = "?1" if "Mobile" in ua else "?0"
        h["Sec-CH-UA-Platform"] = _platform(ua)
        h["Sec-Fetch-Site"] = "none"
        h["Sec-Fetch-Mode"] = "navigate"
        h["Sec-Fetch-User"] = "?1"
        h["Sec-Fetch-Dest"] = "document"
    elif "Edg/" in ua and cv:
        h["Sec-CH-UA"] = f'"Microsoft Edge";v="{cv}", "Chromium";v="{cv}", "Not.A/Brand";v="24"'
        h["Sec-CH-UA-Mobile"] = "?0"
        h["Sec-CH-UA-Platform"] = _platform(ua)
    return h


def random_headers() -> Dict[str, str]:
    return headers_for(random.choice(USER_AGENTS))
